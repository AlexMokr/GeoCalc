package maks.ter.geocalc.model;


public class Block4CapitalSalary {

  private long codeNumber;
  private String region;
  private String proposedSalaryLevel;
  private long vacancies;


  public long getCodeNumber() {
    return codeNumber;
  }

  public void setCodeNumber(long codeNumber) {
    this.codeNumber = codeNumber;
  }


  public String getRegion() {
    return region;
  }

  public void setRegion(String region) {
    this.region = region;
  }


  public String getProposedSalaryLevel() {
    return proposedSalaryLevel;
  }

  public void setProposedSalaryLevel(String proposedSalaryLevel) {
    this.proposedSalaryLevel = proposedSalaryLevel;
  }


  public long getVacancies() {
    return vacancies;
  }

  public void setVacancies(long vacancies) {
    this.vacancies = vacancies;
  }

}
